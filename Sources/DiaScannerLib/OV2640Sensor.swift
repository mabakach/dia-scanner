    /// Applies FrameRate1 CameraSetting from apollo2640.set.
    /// Must be called right before streaming starts.
    public func applyFrameRate1() throws {
        try transport.writeSensorRegister(0xFF, value: 0x01)  // sensor bank
        try transport.writeSensorRegister(0x11, value: 0x07)  // CLKRC: divide by 8
        try transport.writeSensorRegister(0x04, value: 0x33)  // REG04 (from apollo2640.set)
        try transport.writeSensorRegister(0xFF, value: 0x00)  // back to DSP bank
    }

    // ─── Helpers ───────────────────────────────────────────────────────

    enum Bank { case sensor, dsp }

    private func selectBank(_ bank: Bank) throws {
        let v: UInt8 = bank == .sensor ? 0x01 : 0x00
        try transport.writeSensorRegister(Reg.bankSelect, value: v)
    }

    private func softReset() throws {
        try transport.writeSensorRegister(Reg.reset, value: 0x80)
    }

    /// Applies [register, value, mask] triplets to the sensor via I2C.
    private func applySensorTriplets(_ data: Data) throws {
        guard data.count % 3 == 0 else { return }
        for i in stride(from: 0, to: data.count, by: 3) {
            let reg   = data[i]
            let value = data[i + 1]
            let mask  = data[i + 2]

            if mask == 0xFF {
                try transport.writeSensorRegister(reg, value: value)
            } else {
                let current = try transport.readSensorRegister(reg)
                let newVal  = (current & ~mask) | (value & mask)
                try transport.writeSensorRegister(reg, value: newVal)
            }

            // Small delay after soft reset
            if reg == Reg.reset && (value & 0x80) != 0 {
                try Thread.sleep(forTimeInterval: 0.002)
            }
        }
    }
